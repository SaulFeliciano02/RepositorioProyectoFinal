package planes;

public abstract class PlanesTriplePlay extends PlanHogar {
	
	protected String canales;
	protected String velocidad;
	protected String minutos;
	
	public PlanesTriplePlay() {
		super();
		
	}

	public String getCanales() {
		return canales;
	}

	public String getVelocidad() {
		return velocidad;
	}

	public String getMinutos() {
		return minutos;
	}

	
}
